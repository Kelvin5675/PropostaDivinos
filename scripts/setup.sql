-- Create the 'products' table
CREATE TABLE IF NOT EXISTS products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT,
  price TEXT, -- Stored as text to allow "500 MT" or "Sob Consulta"
  category TEXT, -- 'decoração', 'Canecas', 'Papelaria', 'Gráficos'
  image_url TEXT,
  whatsapp_message TEXT, -- Custom message for the whatsapp button
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

